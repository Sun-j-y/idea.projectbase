package Bases._2_BasicConcepts;

public class _14 {
    /*
    * 定义小数
    *   浮点数就是小数
    *   Java中的小数默认数据类型时double
    *   double是Java中保存范围最广的数据类型
    * */
    public static void main(String[] args) {
        double num =10.2;  //10.2是小数, 默认是double类型
        // double型*int型(转换为double, 2.0) = double型
        System.out.println(num);
    }
}
