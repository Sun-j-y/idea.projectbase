package Bases._2_BasicConcepts;

/*
 * 数据类型:
 *      基本数据类型:
 *          数值型
 *              整数类型  byte short int long
 *              浮点类型  float double
 *          字符型  char
 *          布尔型  boolean
 *      引用数据类型: (类似于C/C++中的指针) 在操作时必须进行内存的开辟
 *          类   class
 *          接口 interface
 *          数组
 */
public class DataType {
    byte a = 100;
    short b = 100;
    int c = 43987;
    float d = (float) 73.343;
    long e = 20435868;
    double f = 79851435.516834;
    char g = 'q';
    boolean h = false;
}
